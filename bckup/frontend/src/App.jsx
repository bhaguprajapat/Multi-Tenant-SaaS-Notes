import React, { useEffect, useState } from 'react'
import Login from './pages/Login'
import Notes from './pages/Notes'

export default function App(){
  const [user, setUser] = useState(() => {
    const raw = localStorage.getItem('user');
    return raw ? JSON.parse(raw) : null;
  });

  useEffect(() => {
    if (user) localStorage.setItem('user', JSON.stringify(user));
    else localStorage.removeItem('user');
  }, [user]);

  return (
    <div style={{ maxWidth: 800, margin: '24px auto', fontFamily: 'system-ui, sans-serif' }}>
      <h1>Notes SaaS</h1>
      {user ? <Notes user={user} onLogout={() => { localStorage.removeItem('token'); setUser(null);} } /> : <Login onLogin={(u, token)=> { localStorage.setItem('token', token); setUser(u); }} />}
    </div>
  );
}

